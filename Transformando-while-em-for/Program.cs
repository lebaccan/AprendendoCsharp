using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Transformando_while_em_for
{
    class Program
    {
        static void Main(String[] args)
        {

            for (int contador = 1; contador <= 10; contador++ )
            {
                Console.WriteLine(contador);
            }

            Console.ReadLine();
        }
    }
}
